#include <stdio.h>
#include <ctype.h> // Для функції isspace
//Завдання: Вичислити кількість слів в тексті
//Автор: Ветріченко Дарина Ігорівна
//Група: КН-924з
int main() {
    char text[1000]; // Масив для зберігання тексту
    int wordCount = 0; // Лічильник слів
    int i = 0; // Індекс для перебору символів

    // Введення тексту
    printf("Введіть текст: ");
    fgets(text, sizeof(text), stdin); // Зчитування рядка

    // Перебір символів у тексті
    while (text[i] != '\0') {
        // Якщо символ не є пробілом і це початок слова
        if (!isspace(text[i]) && (i == 0 || isspace(text[i - 1]))) {
            wordCount++; // Збільшуємо лічильник слів
        }
        i++; // Переходимо до наступного символу
    }

    // Виведення результату
    printf("Кількість слів у тексті: %d\n", wordCount);

    return 0; // Завершення програми
}