int main() {
    int ticketNumber; // Змінна для зберігання номера квитка
    int digits[6]; // Масив для зберігання цифр номера квитка
    int firstHalfSum = 0, secondHalfSum = 0; // Змінні для сум першої та другої половини

    // Введення номера квитка
    printf("Введіть шестизначний номер квитка: ");
    scanf("%d", &ticketNumber);

    // Витягування цифр з номера квитка
    for (int i = 5; i >= 0; i--) {
        digits[i] = ticketNumber % 10; // Отримуємо останню цифру
        ticketNumber /= 10; // Видаляємо останню цифру
    }

    // Сумування першої та другої половини
    for (int i = 0; i < 3; i++) {
        firstHalfSum += digits[i]; // Сума перших трьох цифр
        secondHalfSum += digits[i + 3]; // Сума останніх трьох цифр
    }

    // Перевірка на "щасливий" квиток
    if (firstHalfSum == secondHalfSum) {
        printf("Цей квиток є щасливим!\n");
    } else {
        printf("Цей квиток не є щасливим.\n");
    }

    return 0; // Завершення програми
}
